public class DoWhile18 {
    public static void main(String[] args){
        int x = 0;
        do {
            System.out.println(x);
        } while (++x <= 8);
        System.out.println("Program berhenti");

        int x2 = 10;
        do {
            System.out.println(x2);
        } while (++x2 <= 8);
        System.out.println("Program berhenti");
    } 
}

