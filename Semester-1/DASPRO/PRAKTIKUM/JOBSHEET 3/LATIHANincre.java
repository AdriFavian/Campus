public class LATIHANincre{
    public static void main(String[] args) {
        int adri = 19;
//increament
        System.out.println("adri : " + adri);
        System.out.println("++adri : " + ++adri);
        System.out.println("adri++ : " + adri++);
//decreament
        System.out.println("--adri : " + --adri);
        System.out.println("adri-- : " + adri--);
        System.out.println("adri : " + adri);
    }
}